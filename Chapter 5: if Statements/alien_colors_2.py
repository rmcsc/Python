# Choose a color for aline just like 5-3, and write an if-else chain. If
# the color is green, print player earned 5 points. It it isn't green,
# the player earns 10 points. Write a version passing if and another
# passing else.

# Passing green
alien_color = 'green'

if alien_color == 'green':
    print("Player 1 wins 5 points!")
else:
    print("Player 1 wins 10 points!")

# Passing yellow or red
alien_color = 'red'

if alien_color == 'green':
    print("Player 1 wins 5 points!")
else:
    print("Player 1 wins 10 points!")
