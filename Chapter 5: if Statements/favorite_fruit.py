# Try It Yourself 5-7: Favorite Fruit
# Make a list of my three favorite fruits and call it favorite_fruits.
# Write five if statements, checking whether a certain kind of fruit is
# in my list. If it is, the iff block should print a statement like "You
# really like bananas!"

favorite_fruits = ['persimmon', 'banana', 'pear']

if 'apple' in favorite_fruits:
    print("You really like apples!")
if 'pear' in favorite_fruits:
    print("You really like pears!")
if 'persimmon' in favorite_fruits:
    print("You really like persimmons!")
if 'orange' in favorite_fruits:
    print("You really like oranges!")
if 'banana' in favorite_fruits:
    print("You really like bananas!")
