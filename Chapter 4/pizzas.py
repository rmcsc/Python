# Try It Yourself 4-1: Pizzas
# Store 3 kinds of pizza names in a list, then use a for loop to print their names.
# Modify the for loop to print a sentence using the name of the pizza.
# Add a line at the end, outside of the for loop, stating how much you like pizza.

pizzas = ['cheese', 'chicken', 'pepperoni', 'veggie']

for pizza in pizzas:
    print(f"I really love {pizza} pizza!")

print("I really do love all types of pizza.")
